void fill_picture(u8 fill_Data)
{
	u8 m,n;
	for(m=0; m<8; m++)
	{
		Write_IIC_Command(0xb0+m);		//page0-page1
		Write_IIC_Command(0x00);		//low column start address
		Write_IIC_Command(0x10);		//high column start address
		for(n=0; n<128; n++) //132
		{
			Write_IIC_Data(fill_Data);
		}
	}
}
