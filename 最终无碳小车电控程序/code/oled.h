void Picture(u8 *addr)
{
	unsigned char x,y;
	unsigned int i=0;
	for(y=0; y<8; y++)
	{
		Write_IIC_Command(0xb0+y);
		Write_IIC_Command(0x0);
		Write_IIC_Command(0x10);
		for(x=0; x<128; x++)
		{
			Write_IIC_Data(addr[i++]);
		}
	}
}
